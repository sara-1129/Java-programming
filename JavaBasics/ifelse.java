package JavaBasics;

public class ifelse {
    public static void main(String[] args) {
        //conditonal statements 
        //ifelse
        boolean isSunUp = true;
        if (isSunUp) {
            System.out.println("It is day.");
        }
        else {
            System.out.println("It is night.");
        }
        int age = 30;
        if(age >= 18)
        {
            System.out.println("Can Vote");
        }
        else{
            System.out.println("Cannot Vote");
        }
    }
}
